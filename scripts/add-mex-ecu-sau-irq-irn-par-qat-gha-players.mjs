const COUNTRIES = [
  { teamId: "MEX", idPrefix: "mex", roster: MEX, replace: true },
  { teamId: "ECU", idPrefix: "ecu", roster: ECU },
  // teams.json は FIFA 正式コードの KSA (Kingdom of Saudi Arabia) を使う。
  // 初回実行時に SAU で登録してしまい、後から fix-sau-to-ksa.mjs で修正した。
  { teamId: "KSA", idPrefix: "ksa", roster: SAU },
  { teamId: "IRQ", idPrefix: "irq", roster: IRQ },
  { teamId: "IRN", idPrefix: "irn", roster: IRN },
  { teamId: "PAR", idPrefix: "par", roster: PAR },
  { teamId: "QAT", idPrefix: "qat", roster: QAT },
  { teamId: "GHA", idPrefix: "gha", roster: GHA },
];

const raw = await readFile(FILE, "utf8");
let players = JSON.parse(raw);

let added = 0;
let removed = 0;
for (const c of COUNTRIES) {
  if (c.replace) {
    const before = players.length;
    players = players.filter((p) => p.teamId !== c.teamId);
    removed += before - players.length;
  } else {
    const existing = players.filter((p) => p.teamId === c.teamId);
    if (existing.length > 0) {
      console.warn(`skip ${c.teamId}: 既に ${existing.length} 名登録されています`);
      continue;
    }
  }
  c.roster.forEach(([position, name, birthDate, club], i) => {
    players.push({
      id: `p_${c.idPrefix}_${i + 1}`,
      name,
      teamId: c.teamId,
      position,
      birthDate,
      club,
      goals: 0,
      assists: 0,
    });
    added++;
  });
}

function fmtPlayer(p) {
  const entries = Object.entries(p).map(
    ([k, v]) => `"${k}": ${JSON.stringify(v)}`
  );
  return `{ ${entries.join(", ")} }`;
}

const lines = ["["];
let prevTeam = null;
for (let i = 0; i < players.length; i++) {
  const p = players[i];
  if (prevTeam !== null && p.teamId !== prevTeam) lines.push("");
  prevTeam = p.teamId;
  lines.push("  " + fmtPlayer(p) + (i < players.length - 1 ? "," : ""));
}
lines.push("]");
await writeFile(FILE, lines.join("\n") + "\n", "utf8");

console.log(`削除: ${removed} 名 / 追加: ${added} 名 / 総数: ${players.length} 名`);
